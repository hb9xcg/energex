#ifndef TYPES_H_
#define TYPES_H_

#include <QtGlobal>

typedef qint8 int8_t;			// signed byte

typedef qint16 int16_t;	    // signed 16-bit integer
typedef qint16 int16;	    	// signed 16-bit integer

typedef qint32 int32_t;		// signed 32-bit integer

typedef qint64 int64_t;		// signed 64-bit integer

typedef quint8 uint8_t;		// unsigned byte
typedef quint8 uint8;			// unsigned byte

typedef quint16 uint16_t;		// unsigned 16-bit integer

typedef quint32 uint32_t;		// unsigned 32-bit integer

typedef quint64 uint64_t;		// unsigned 64-bit integer

#endif /*TYPES_H_*/
